#include <stdint.h>

unsigned udiv_5_u32(unsigned a)
{
   return a / 5u;
}

unsigned urem_5_u32(unsigned a)
{
   return a % 5u;
}

unsigned udiv_23_u32(unsigned a)
{
   return a / 23u;
}

unsigned urem_23_u32(unsigned a)
{
   return a % 23u;
}

unsigned long long udiv_9_u64(unsigned long long a)
{
   return a / 9ULL;
}

unsigned long long urem_9_u64(unsigned long long a)
{
   return a % 9ULL;
}
